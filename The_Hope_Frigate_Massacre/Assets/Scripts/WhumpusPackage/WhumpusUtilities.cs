using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class WhumpusUtilities 
{
    public static int ToLayer(int bitmask)
    {
        int result = bitmask > 0 ? 0 : 31;
        while (bitmask > 1)
        {
            bitmask = bitmask >> 1;
            result++;
        }
        return result;
    }
}
